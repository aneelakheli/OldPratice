class ArrayDefine {
    public static void main(String args[]) {
        int a1[] = new int[10];
        int a2[] = { 1, 3, 5, 7, 9, 11, 13, 17 };
        int a3[] = { 2, 4, 6, 8 };
        System.out.println("length of a1 is :" + a1.length);
        System.out.println("length of a2 is :" + a2.length);
        System.out.println("length of a3 is :" + a3.length);
    }
}